import "./bootstrap";
import "../css/app.css";

import { createRoot } from "react-dom/client";
import { createInertiaApp } from "@inertiajs/react";
import { resolvePageComponent } from "laravel-vite-plugin/inertia-helpers";

const appName = import.meta.env.VITE_APP_NAME || "SENADA";

createInertiaApp({
    title: (title) => `${title} - ${appName}`,
    resolve: (name) =>
        resolvePageComponent(
            `./Pages/${name}.jsx`,
            import.meta.glob("./Pages/**/*.jsx"),
        ),
    setup({ el, App, props }) {
        const root = createRoot(el);
        root.render(<App {...props} />);
    },
    progress: {
        // UBAH WARNA DI SINI MENJADI HIJAU SENADA
        color: "#0e6858",
        // Tambahkan ketebalan jika dirasa terlalu tipis (opsional, default 29)
        showSpinner: true, // Akan memunculkan loading spinner kecil di pojok kanan atas browser
    },
});
